    private void addBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBTNActionPerformed
        // TODO add your handling code here:
        
        String newname = newNameField.getText();
        String role = newRoleField.getText();
        String dep = newDepField.getText();
        try{
        Double salary = Double.valueOf(newSalField.getText());
        if(salary<0){
            throw new NumberFormatException ( "Please enter a valid input (salary or password)");
        }
        String pass = newPassField.getText();
        Employee e = new Employee(newname, role, dep, salary, pass);
        c.managerAccess.addEmployee(e);
        tasksTextArea.setText("Employee added suscessfly! \n" + e.toString());
        } catch (NullPointerException e) {
            tasksTextArea.setText("please add informations.");
        } catch (NumberFormatException e) {
            tasksTextArea.setText("Please enter a valid input (salary or password)\npassword should be equal to or more than 8 characters");
        }
    }//GEN-LAST:event_addBTNActionPerformed

