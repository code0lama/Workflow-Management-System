    private void kickbuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_kickbuttonActionPerformed
        // TODO add your handling code here:
        String ename = enameFielf.getText();

        try {
            if (c.company.findNode(name).employees.search(ename) == null) {
                throw new NullPointerException("No employee with such name in your department.");
            }

            c.managerAccess.removeEmployee(ename);
            eTextArea.setText("Employee kicked successfully!");

        } catch (NullPointerException e) {
            eTextArea.setText("No employee with such name in your department.");
        } catch (NumberFormatException e) {
            tasksTextArea.setText("Please enter a valid salary.");
        }
    }//GEN-LAST:event_kickbuttonActionPerformed

