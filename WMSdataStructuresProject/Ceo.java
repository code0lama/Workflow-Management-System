    private void SendBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SendBTNActionPerformed
        // TODO add your handling code here:
        String ename = mnameField.getText();
        String title = titleBTN.getText();
        String date = dateBTN.getText();
        
        String[] dates = date.split("-");
        try {
            if (c.company.findNode(ename).getData().getName() == null) {
                throw new NullPointerException("No manager with such name.");
            }

            int year = Integer.parseInt(dates[0]);
            int month = Integer.parseInt(dates[1]);
            int day = Integer.parseInt(dates[2]);
            Date dueDate = new Date(year, month - 1, day);
            Task task = new Task(title, dueDate);
            c.ceoAccess.SendTaskToManager(ename, task);
            ceTextArea.setText(c.ceoAccess.ShowManagerTasks(ename));

        } catch (NullPointerException e) {
            ceTextArea.setText("No manager with such name.");
        } catch (NumberFormatException e) {
            ceTextArea.setText("Please enter a valid date.");
        }

    }//GEN-LAST:event_SendBTNActionPerformed

