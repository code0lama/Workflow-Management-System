    private void kickbuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_kickbuttonActionPerformed
        // TODO add your handling code here:
        String ename = mnameField.getText();

        try {
            if (c.company.findNode(ename).getData().getName() == null) {
                throw new NullPointerException("No manager with such name.");
            }
            String newname = newNameField.getText();
            String role = newRoleField.getText();
            String dep = newDepField.getText();
            Double salary = Double.valueOf(newSalField.getText());
            if(salary<0){
            throw new NumberFormatException ( "Please enter a valid salary.");
        }
            String pass = newPassField.getText();
            Employee e = new Employee(newname, role, dep, salary, pass);
            c.ceoAccess.FireManager(ename, e);
            ceTextArea.setText("Manager fired and replaced successfully!\n\nNew manager:\n" + e.toString());

        } catch (NullPointerException e) {
            ceTextArea.setText("No manager with such name.");
        }catch (NumberFormatException e) {
            ceTextArea.setText("Please enter a valid input (salary or password)\npassword should be equal to or more than 8 characters");
        }
    }//GEN-LAST:event_kickbuttonActionPerformed

